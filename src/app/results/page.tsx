"use client";

import { useEffect, useState } from "react";
import { useRouter } from "next/navigation";
import Link from "next/link";

type AdviceResult = {
  type: "advice";
  input: { crop: string; stage: string; location: string; soil: string; problem: string };
  diagnosis: string;
  immediateActions: string[];
  weekPlan: string[];
  products: string[];
  warnings: string[];
  confidence: number;
};

type DiagnosisResult = {
  type: "diagnosis";
  cropType: string;
  imagePreview?: string;
  disease: string;
  severity: "low" | "medium" | "high";
  description: string;
  treatment: string[];
  organic: string[];
  chemical: string[];
  prevention: string[];
};

type Result = AdviceResult | DiagnosisResult;

const severityColor = {
  low: "bg-[#84cc16]/20 text-[#84cc16] border-[#84cc16]/30",
  medium: "bg-[#f59e0b]/20 text-[#f59e0b] border-[#f59e0b]/30",
  high: "bg-red-900/30 text-red-400 border-red-700/30",
};

export default function ResultsPage() {
  const router = useRouter();
  const [result, setResult] = useState<Result | null>(null);
  const [followUp, setFollowUp] = useState("");
  const [followUpLoading, setFollowUpLoading] = useState(false);
  const [followUpAnswer, setFollowUpAnswer] = useState("");
  const [checkedItems, setCheckedItems] = useState<Set<string>>(new Set());

  useEffect(() => {
    const timer = window.setTimeout(() => {
      const stored = sessionStorage.getItem("agri_result");
      if (!stored) { router.push("/advice"); return; }
      setResult(JSON.parse(stored));
    }, 0);
    return () => window.clearTimeout(timer);
  }, [router]);

  function toggleCheck(item: string) {
    setCheckedItems(prev => {
      const next = new Set(prev);
      if (next.has(item)) next.delete(item);
      else next.add(item);
      return next;
    });
  }

  async function handleFollowUp(e: React.FormEvent) {
    e.preventDefault();
    if (!followUp.trim() || result?.type !== "advice") return;
    setFollowUpLoading(true);
    try {
      const { input } = result as AdviceResult;
      const res = await fetch("/api/advise", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ ...input, problem: `Follow-up: ${followUp}. Previous diagnosis: ${result.diagnosis}` }),
      });
      const data = await res.json();
      setFollowUpAnswer(data.diagnosis + "\n\nImmediate actions:\n" + data.immediateActions.join("\n"));
      setFollowUp("");
    } catch {
      setFollowUpAnswer("Sorry, couldn't get a response. Please try again.");
    } finally {
      setFollowUpLoading(false);
    }
  }

  function saveReport() {
    if (!result) return;
    const lines: string[] = ["AgriAI Farm Report", "=".repeat(40), ""];
    if (result.type === "advice") {
      lines.push(`Crop: ${result.input.crop} | Stage: ${result.input.stage}`);
      lines.push(`Location: ${result.input.location} | Soil: ${result.input.soil}`);
      lines.push(`Problem: ${result.input.problem}`);
      lines.push("", "DIAGNOSIS:", result.diagnosis);
      lines.push("", "IMMEDIATE ACTIONS:", ...result.immediateActions.map(a => `• ${a}`));
      lines.push("", "WEEK PLAN:", ...result.weekPlan.map(a => `• ${a}`));
      lines.push("", "PRODUCTS:", ...result.products.map(a => `• ${a}`));
      lines.push("", "WARNINGS:", ...result.warnings.map(a => `• ${a}`));
      lines.push("", `Confidence: ${result.confidence}%`);
    } else {
      lines.push(`Crop: ${result.cropType}`);
      lines.push(`Disease: ${result.disease} | Severity: ${result.severity.toUpperCase()}`);
      lines.push("", "DESCRIPTION:", result.description);
      lines.push("", "TREATMENT:", ...result.treatment.map(a => `• ${a}`));
      lines.push("", "ORGANIC OPTIONS:", ...result.organic.map(a => `• ${a}`));
      lines.push("", "CHEMICAL OPTIONS:", ...result.chemical.map(a => `• ${a}`));
      lines.push("", "PREVENTION:", ...result.prevention.map(a => `• ${a}`));
    }
    lines.push("", `Generated by AgriAI — ${new Date().toLocaleDateString()}`);
    const blob = new Blob([lines.join("\n")], { type: "text/plain" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = `agri-report-${Date.now()}.txt`;
    a.click();
    URL.revokeObjectURL(url);
  }

  if (!result) return (
    <div className="min-h-screen flex items-center justify-center text-[#fef9c3]/50">
      Loading...
    </div>
  );

  return (
    <main className="min-h-screen px-4 py-6 max-w-xl mx-auto pb-16">
      {/* Header */}
      <div className="flex items-center justify-between mb-6">
        <Link href="/advice" className="text-[#84cc16]/70 text-sm hover:text-[#84cc16] transition-colors">← New Advice</Link>
        <button
          onClick={saveReport}
          className="flex items-center gap-1.5 text-sm text-[#f59e0b] border border-[#f59e0b]/30 px-3 py-1.5 rounded-lg hover:bg-[#f59e0b]/10 transition-colors"
        >
          <svg className="w-4 h-4" fill="none" stroke="currentColor" strokeWidth={2} viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" d="M3 16.5v2.25A2.25 2.25 0 0 0 5.25 21h13.5A2.25 2.25 0 0 0 21 18.75V16.5M16.5 12 12 16.5m0 0L7.5 12m4.5 4.5V3" />
          </svg>
          Save Report
        </button>
      </div>

      {result.type === "diagnosis" ? (
        <DiagnosisView result={result} checkedItems={checkedItems} onToggle={toggleCheck} />
      ) : (
        <AdviceView result={result} checkedItems={checkedItems} onToggle={toggleCheck} />
      )}

      {/* Follow-up — only for advice type */}
      {result.type === "advice" && (
        <section className="mt-6">
          <h3 className="font-bold text-[#fef9c3] mb-3">Ask a Follow-up Question</h3>
          <form onSubmit={handleFollowUp} className="flex flex-col gap-3">
            <textarea
              value={followUp}
              onChange={e => setFollowUp(e.target.value)}
              rows={2}
              placeholder="e.g. What if the yellow spots spread to the top leaves?"
              className="w-full bg-[#0f2200] border border-[#1a3300] rounded-xl px-4 py-3 text-[#fef9c3] placeholder-[#fef9c3]/30 focus:outline-none focus:border-[#84cc16]/60 transition-colors text-sm resize-none"
            />
            <button
              type="submit"
              disabled={followUpLoading || !followUp.trim()}
              className="py-3 bg-[#1a3300] text-[#84cc16] font-semibold rounded-xl border border-[#84cc16]/30 hover:bg-[#84cc16]/10 transition-colors disabled:opacity-40 text-sm"
            >
              {followUpLoading ? "Thinking..." : "Ask Agronomist"}
            </button>
          </form>
          {followUpAnswer && (
            <div className="mt-4 bg-[#0f2200] border border-[#1a3300] rounded-xl p-4 text-[#fef9c3]/80 text-sm whitespace-pre-line leading-relaxed">
              {followUpAnswer}
            </div>
          )}
        </section>
      )}
    </main>
  );
}

function AdviceView({ result, checkedItems, onToggle }: { result: AdviceResult; checkedItems: Set<string>; onToggle: (s: string) => void }) {
  return (
    <div className="flex flex-col gap-4">
      {/* Diagnosis card */}
      <Card title="Diagnosis">
        <div className="flex items-center justify-between mb-2">
          <span className="text-[#fef9c3]/50 text-xs">Confidence</span>
          <span className={`text-sm font-bold ${result.confidence >= 70 ? "text-[#84cc16]" : result.confidence >= 50 ? "text-[#f59e0b]" : "text-red-400"}`}>
            {result.confidence}%
          </span>
        </div>
        <div className="w-full bg-[#1a3300] rounded-full h-1.5 mb-3">
          <div className="bg-[#84cc16] h-1.5 rounded-full transition-all" style={{ width: `${result.confidence}%` }} />
        </div>
        <p className="text-[#fef9c3]/80 text-sm leading-relaxed">{result.diagnosis}</p>
      </Card>

      <Card title="Do This Today">
        <Checklist items={result.immediateActions} checkedItems={checkedItems} onToggle={onToggle} accent="#84cc16" />
      </Card>

      <Card title="This Week's Plan">
        <Checklist items={result.weekPlan} checkedItems={checkedItems} onToggle={onToggle} accent="#f59e0b" />
      </Card>

      <Card title="Recommended Products">
        {result.products.map((p, i) => {
          const isOrganic = p.toLowerCase().includes("organic") || p.toLowerCase().includes("neem") || p.toLowerCase().includes("copper") || p.toLowerCase().includes("epsom") || p.toLowerCase().includes("fish") || p.toLowerCase().includes("bacillus");
          return (
            <div key={i} className="flex items-start gap-2 py-2 border-b border-[#1a3300] last:border-0">
              <span className={`mt-0.5 text-xs px-2 py-0.5 rounded-full font-medium flex-shrink-0 ${isOrganic ? "bg-[#84cc16]/15 text-[#84cc16]" : "bg-[#f59e0b]/15 text-[#f59e0b]"}`}>
                {isOrganic ? "organic" : "chemical"}
              </span>
              <span className="text-[#fef9c3]/80 text-sm">{p}</span>
            </div>
          );
        })}
      </Card>

      {result.warnings.length > 0 && (
        <div className="bg-red-900/20 border border-red-700/30 rounded-2xl p-4">
          <h3 className="font-bold text-red-400 text-sm mb-2 flex items-center gap-1.5">
            <svg className="w-4 h-4" fill="none" stroke="currentColor" strokeWidth={2} viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" d="M12 9v3.75m-9.303 3.376c-.866 1.5.217 3.374 1.948 3.374h14.71c1.73 0 2.813-1.874 1.948-3.374L13.949 3.378c-.866-1.5-3.032-1.5-3.898 0L2.697 16.126ZM12 15.75h.007v.008H12v-.008Z" />
            </svg>
            Watch For
          </h3>
          <ul className="space-y-1">
            {result.warnings.map((w, i) => (
              <li key={i} className="text-red-300/80 text-sm flex items-start gap-1.5">
                <span className="mt-1 flex-shrink-0">•</span> {w}
              </li>
            ))}
          </ul>
        </div>
      )}
    </div>
  );
}

function DiagnosisView({ result, checkedItems, onToggle }: { result: DiagnosisResult; checkedItems: Set<string>; onToggle: (s: string) => void }) {
  return (
    <div className="flex flex-col gap-4">
      {/* Disease header */}
      <div className="bg-[#0f2200] border border-[#1a3300] rounded-2xl p-5">
        {result.imagePreview && (
          // eslint-disable-next-line @next/next/no-img-element
          <img src={result.imagePreview} alt="Scanned plant" className="w-full rounded-xl object-cover max-h-48 mb-4" />
        )}
        <div className="flex items-start justify-between gap-3">
          <div>
            <p className="text-[#fef9c3]/50 text-xs mb-1">Identified Disease</p>
            <h2 className="text-[#fef9c3] font-bold text-lg leading-tight">{result.disease}</h2>
          </div>
          <span className={`text-sm font-bold px-3 py-1 rounded-full border flex-shrink-0 ${severityColor[result.severity]}`}>
            {result.severity.toUpperCase()}
          </span>
        </div>
        <p className="mt-3 text-[#fef9c3]/70 text-sm leading-relaxed">{result.description}</p>
      </div>

      <Card title="Treatment Steps">
        <Checklist items={result.treatment} checkedItems={checkedItems} onToggle={onToggle} accent="#84cc16" />
      </Card>

      <Card title="Organic Treatments">
        {result.organic.map((o, i) => (
          <div key={i} className="flex items-start gap-2 py-2 border-b border-[#1a3300] last:border-0">
            <span className="mt-0.5 text-xs px-2 py-0.5 rounded-full font-medium bg-[#84cc16]/15 text-[#84cc16] flex-shrink-0">organic</span>
            <span className="text-[#fef9c3]/80 text-sm">{o}</span>
          </div>
        ))}
      </Card>

      {result.chemical.length > 0 && (
        <Card title="Chemical Treatments">
          {result.chemical.map((c, i) => (
            <div key={i} className="flex items-start gap-2 py-2 border-b border-[#1a3300] last:border-0">
              <span className="mt-0.5 text-xs px-2 py-0.5 rounded-full font-medium bg-[#f59e0b]/15 text-[#f59e0b] flex-shrink-0">chemical</span>
              <span className="text-[#fef9c3]/80 text-sm">{c}</span>
            </div>
          ))}
        </Card>
      )}

      <Card title="Prevention for Next Season">
        <Checklist items={result.prevention} checkedItems={checkedItems} onToggle={onToggle} accent="#f59e0b" />
      </Card>
    </div>
  );
}

function Card({ title, children }: { title: string; children: React.ReactNode }) {
  return (
    <div className="bg-[#0f2200] border border-[#1a3300] rounded-2xl p-4">
      <h3 className="font-bold text-[#fef9c3] text-sm mb-3">{title}</h3>
      {children}
    </div>
  );
}

function Checklist({ items, checkedItems, onToggle, accent }: {
  items: string[];
  checkedItems: Set<string>;
  onToggle: (s: string) => void;
  accent: string;
}) {
  return (
    <ul className="space-y-2">
      {items.map((item, i) => {
        const key = `${i}-${item}`;
        const checked = checkedItems.has(key);
        return (
          <li key={key}>
            <button
              onClick={() => onToggle(key)}
              className="w-full flex items-start gap-3 text-left group"
            >
              <span
                className="mt-0.5 w-5 h-5 rounded-full border-2 flex-shrink-0 flex items-center justify-center transition-all"
                style={{ borderColor: checked ? accent : "#1a3300", backgroundColor: checked ? accent : "transparent" }}
              >
                {checked && (
                  <svg className="w-3 h-3 text-[#0c1a00]" fill="none" stroke="currentColor" strokeWidth={3} viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" d="m4.5 12.75 6 6 9-13.5" />
                  </svg>
                )}
              </span>
              <span className={`text-sm leading-relaxed transition-colors ${checked ? "line-through text-[#fef9c3]/30" : "text-[#fef9c3]/80"}`}>
                {item}
              </span>
            </button>
          </li>
        );
      })}
    </ul>
  );
}
